package dayTwo60;

public class Homework04 {
    public static void main(String[] args){
        //保存两本书名，用+拼接
        String book1 = "天龙八部";
        String book2 = "笑傲江湖";
        System.out.println(book1 + book2);
        //保存两个性别，用+拼接
        char name1 = '男';
        char name2 = '女';
        System.out.println(name1 + name2);//码值相加
        //保存两本书价格，用+拼接
        double price1 = 66.6;
        double price2 = 88.8;
        System.out.println(price1 + price2);//结果为金近似值

    }
}
